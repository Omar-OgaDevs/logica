var y;
y = 13
document.write("<br/>");
document.write("El valor de x = : " + x + "<br/>");
document.write("El valor de y = : " + y + "<br/>");

//El operador + se utiliza para sumar 
var z = x + y;
document.write("<br/>");
document.write("El valor de z es: " + z +"<br/>");

//El operador ++ se utiliza para incrementar en 1
//El operador -- se utiliza para disminuir en 1
x++;
document.write("<br/>");
document.write("El valor de x++ es: " + x +"<br/>");

//El operador +=
//x= x + y; es igual a:
x += y;
document.write("<br/>");
document.write("El valor de x es: " + x + "<br/>");

